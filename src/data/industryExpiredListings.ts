// Export function to get expired listings by industry
export const getExpiredListingsByIndustry = (industry: string): IndustryListing[] => {
  switch (industry.toLowerCase()) {
    case 'nails':
      return expiredNailListings;
    case 'hair':
      return expiredHairListings;
    case 'barber':
      return expiredBarberListings;
    case 'massage':
      return expiredMassageListings;
    case 'makeup':
      return expiredMakeupListings;
    case 'tattoo':
      return expiredTattooListings;
    case 'skincare':
    case 'facial':
      return expiredFacialListings;
    case 'brows-lashes':
    case 'lashes':
      return expiredBrowLashListings;
    default:
      return [];
  }
};